#include <iostream>
using namespace std;

// --------------------
// 1.4. Print a Table.
// by : Putra
// --------------------

int main() {
    cout << "Table. The Product of 5 and 10." << endl;
    cout << "==========================" << endl;
    cout << "|  " << "p" << "   | " << "p * 5" << "  | " << "p * 10" << " |"<< endl;
    cout << "--------------------------" << endl;
    cout << "|   " << 5 << "  |  " << 5 * 5 << "    |  " << 5 * 10 << "    |" << endl;
    cout << "|  " << 10 << "  |  " << 10 * 5 << "    | "<< 10 * 10 << "    |" << endl;
    cout << "|  " << 15 << "  |  " << 15 * 5 << "    | " << 15 * 10 << "    |" << endl;
    cout << "|  " << 20 << "  | " << 20 * 5 << "    | " << 20 * 10 << "    |" << endl;
    cout << "|  " << 25 << "  | " << 25 * 5 << "    | " << 25 * 10 << "    |" << endl;
    cout << "==========================" << endl;
    return 0;
}

